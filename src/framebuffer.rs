/// Get the 8x8 glyph for a character. Falls back to a filled box for unknown chars.
fn glyph(ch: u8) -> [u8; 8] {
    if ch >= 0x20 && ch <= 0x7E {
        FONT_8X8[(ch - 0x20) as usize]
    } else {
        // Unknown character — draw a filled box
        [0xFF, 0x81, 0x81, 0x81, 0x81, 0x81, 0xFF, 0x00]
    }
}

/// Draw a character at (x, y) with a given scale factor
pub fn draw_char(fb: &Framebuffer, ch: u8, x: u32, y: u32, scale: u32, color: Pixel) {
    let g = glyph(ch);
    for row in 0..8u32 {
        for col in 0..8u32 {
            if g[row as usize] & (0x80 >> col) != 0 {
                fb.fill_rect(x + col * scale, y + row * scale, scale, scale, color);
            }
        }
    }
}

/// Draw a string at (x, y)
pub fn draw_string(fb: &Framebuffer, s: &str, x: u32, y: u32, scale: u32, color: Pixel) {
    let char_width = 8 * scale + scale; // 8 pixels + 1 pixel gap, scaled
    for (i, ch) in s.bytes().enumerate() {
        draw_char(fb, ch, x + (i as u32) * char_width, y, scale, color);
    }
}

// =============================================================================
// Global framebuffer storage
// =============================================================================

static mut GLOBAL_FB: Option<Framebuffer> = None;

/// Get a reference to the global framebuffer, if initialized.
#[allow(static_mut_refs)]
pub fn get() -> Option<&'static Framebuffer> {
    unsafe { GLOBAL_FB.as_ref() }
}

// =============================================================================
// ramfb initialisation + graph rendering
// =============================================================================

pub fn init() {
    crate::println!("[fb] Initialising ramfb framebuffer...");

    let info = match crate::ramfb::init() {
        Some(info) => info,
        None => {
            crate::println!("[fb] No ramfb available — UART only");
            return;
        }
    };

    let fb = Framebuffer {
        base: info.fb_ptr,
        width: info.width,
        height: info.height,
        stride: info.width * 4,
        bpp: 4,
    };

    // Store globally
    unsafe {
        GLOBAL_FB = Some(fb);
    }

    // Render the graph visualization
    render_graph();

    crate::println!("[fb] Graph rendered. Framebuffer ready.");
}

/// Re-render the graph on the framebuffer. Called from init and from shell.
pub fn render_graph() {
    if let Some(fb) = get() {
        let prev = crate::arch::riscv64::interrupts_disable();
        let graph = crate::graph::get();
        crate::graph::render::render(fb, graph);
        crate::arch::riscv64::interrupts_restore(prev);
    }
}
