/// Framebuffer abstraction for Helios.
///
/// Uses ramfb (a simple QEMU display device) to provide a framebuffer.
/// The guest allocates memory, writes a config via fw_cfg, and QEMU displays it.
/// No virtqueues or command protocols needed.

/// A simple RGBA pixel
#[derive(Clone, Copy)]
#[repr(C)]
pub struct Pixel {
    pub r: u8,
    pub g: u8,
    pub b: u8,
    pub a: u8,
}

impl Pixel {
    pub const fn new(r: u8, g: u8, b: u8) -> Self {
        Self { r, g, b, a: 255 }
    }
}

/// Framebuffer info
pub struct Framebuffer {
    pub base: *mut u8,
    pub width: u32,
    pub height: u32,
    pub stride: u32, // bytes per row
    pub bpp: u32,    // bytes per pixel
}

// Safety: framebuffer memory is a fixed hardware resource, accessed single-threaded.
unsafe impl Send for Framebuffer {}
unsafe impl Sync for Framebuffer {}

impl Framebuffer {
    /// Fill the entire framebuffer with a solid color
    pub fn fill(&self, pixel: Pixel) {
        if self.base.is_null() {
            return;
        }
        for y in 0..self.height {
            for x in 0..self.width {
                self.put_pixel(x, y, pixel);
            }
        }
    }

    /// Set a single pixel
    pub fn put_pixel(&self, x: u32, y: u32, pixel: Pixel) {
        if self.base.is_null() || x >= self.width || y >= self.height {
            return;
        }
        let offset = (y * self.stride + x * self.bpp) as usize;
        unsafe {
            let ptr = self.base.add(offset);
            // BGRA / XRGB format (common for virtio-gpu / ramfb)
            ptr.add(0).write_volatile(pixel.b);
            ptr.add(1).write_volatile(pixel.g);
            ptr.add(2).write_volatile(pixel.r);
            ptr.add(3).write_volatile(pixel.a);
        }
    }

    /// Draw a filled rectangle
    pub fn fill_rect(&self, x: u32, y: u32, w: u32, h: u32, pixel: Pixel) {
        for dy in 0..h {
            for dx in 0..w {
                self.put_pixel(x + dx, y + dy, pixel);
            }
        }
    }

    /// Draw a rectangle outline (border only)
    pub fn draw_rect_outline(&self, x: u32, y: u32, w: u32, h: u32, pixel: Pixel) {
        if w == 0 || h == 0 {
            return;
        }
        // Top and bottom
        for dx in 0..w {
            self.put_pixel(x + dx, y, pixel);
            self.put_pixel(x + dx, y + h - 1, pixel);
        }
        // Left and right
        for dy in 1..h - 1 {
            self.put_pixel(x, y + dy, pixel);
            self.put_pixel(x + w - 1, y + dy, pixel);
        }
    }

    /// Draw a horizontal line
    pub fn draw_hline(&self, x: u32, y: u32, len: u32, pixel: Pixel) {
        for dx in 0..len {
            self.put_pixel(x + dx, y, pixel);
        }
    }

    /// Draw a vertical line
    pub fn draw_vline(&self, x: u32, y: u32, len: u32, pixel: Pixel) {
        for dy in 0..len {
            self.put_pixel(x, y + dy, pixel);
        }
    }

    /// Draw an arbitrary line using Bresenham's algorithm
    pub fn draw_line(&self, x0: u32, y0: u32, x1: u32, y1: u32, pixel: Pixel) {
        let mut x0 = x0 as i32;
        let mut y0 = y0 as i32;
        let x1 = x1 as i32;
        let y1 = y1 as i32;

        let dx = (x1 - x0).abs();
        let dy = -(y1 - y0).abs();
        let sx: i32 = if x0 < x1 { 1 } else { -1 };
        let sy: i32 = if y0 < y1 { 1 } else { -1 };
        let mut err = dx + dy;

        loop {
            if x0 >= 0 && y0 >= 0 {
                self.put_pixel(x0 as u32, y0 as u32, pixel);
            }
            if x0 == x1 && y0 == y1 { break; }
            let e2 = 2 * err;
            if e2 >= dy {
                err += dy;
                x0 += sx;
            }
            if e2 <= dx {
                err += dx;
                y0 += sy;
            }
        }
    }
}

